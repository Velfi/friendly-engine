test "appendTextQuads emits glyph pixels" {
    var quads: std.ArrayList(gpu_scene.OverlayQuad) = .empty;
    defer quads.deinit(std.testing.allocator);
    try appendTextQuads("A", 0, 0, .{ .r = 255, .g = 255, .b = 255, .a = 255 }, &quads, std.testing.allocator);
    try std.testing.expect(quads.items.len > 0);
}
