const std = @import("std");
const shared_color = @import("color.zig");
const gpu_scene = @import("gpu_scene.zig");

pub const GlyphSize = struct { w: f32 = 6.0, h: f32 = 8.0, advance: f32 = 7.0 };

pub fn appendTextQuads(
    text: []const u8,
    rect_x: f32,
    rect_y: f32,
    color: shared_color.Color,
    out: *std.ArrayList(gpu_scene.OverlayQuad),
    allocator: std.mem.Allocator,
) !void {
    try appendTextQuadsBounded(text, rect_x, rect_y, null, color, out, allocator);
}

pub fn appendTextQuadsBounded(
    text: []const u8,
    rect_x: f32,
    rect_y: f32,
    max_width: ?f32,
    color: shared_color.Color,
    out: *std.ArrayList(gpu_scene.OverlayQuad),
    allocator: std.mem.Allocator,
) !void {
    var x = rect_x + 2.0;
    const y = rect_y + 2.0;
    const glyph = GlyphSize{};
    const max_x: ?f32 = if (max_width) |width| rect_x + @max(0, width) else null;
    for (text) |ch| {
        if (max_x) |limit| {
            if (x >= limit) break;
        }
        if (ch == ' ') {
            x += glyph.advance;
            continue;
        }
        if (ch < 32 or ch > 126) continue;
        if (max_x) |limit| {
            if (x + glyph.advance > limit) break;
        }
        const glyph_index: usize = ch - 32;
        const pattern = glyph_patterns[glyph_index];
        var row: u32 = 0;
        while (row < 7) : (row += 1) {
            var col: u32 = 0;
            while (col < 5) : (col += 1) {
                const bit: u32 = @as(u32, 1) << @as(u5, @intCast(4 - col));
                if (pattern[row] & bit != 0) {
                    try out.append(allocator, .{
                        .rect = .{
                            x + @as(f32, @floatFromInt(col)),
                            y + @as(f32, @floatFromInt(row)),
                            1.0,
                            1.0,
                        },
                        .color = color,
                    });
                }
            }
        }
        x += glyph.advance;
    }
}

// 5x7 bitmap font for ASCII 32..126 (95 glyphs)
const glyph_patterns = blk: {
    @setEvalBranchQuota(10000);
    var patterns: [95][7]u8 = undefined;
    for (0..95) |i| {
        const ch: u8 = @intCast(32 + i);
        patterns[i] = glyphForChar(ch);
    }
    break :blk patterns;
};

